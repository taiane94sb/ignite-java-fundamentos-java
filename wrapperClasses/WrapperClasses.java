package wrapperClasses;

public class WrapperClasses {
    public static void main(String[] args) {
        int tipoInt = 1;
        float tipoFloat = 1.0f;
        boolean tipoBoolean = false;

        Integer tipoInt2 = 1;
        Float tipoFloat2 = 1.0F;
        Boolean tipoBoolean2 = false;
        
        System.out.println(tipoInt);
        System.out.println(tipoFloat);
        System.out.println(tipoBoolean);

        System.out.println(tipoInt2);
        System.out.println(tipoFloat2);
        System.out.println(tipoBoolean2);
    }
}
